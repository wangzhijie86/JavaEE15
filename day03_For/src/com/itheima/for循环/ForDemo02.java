package com.itheima.for循环;

public class ForDemo02 {
    public static void main(String[] args) {
        //使用for循环实现1到5和5到1的输出
        for (int i = 1; i < 6; i++) {
            System.out.println(i);
        }
        System.out.println("*******************");
        for (int i = 6; i > 1; i--) {
            System.out.println(i);
        }
    }
}
