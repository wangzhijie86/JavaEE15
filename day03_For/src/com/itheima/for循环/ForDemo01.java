package com.itheima.for循环;

public class ForDemo01 {
    public static void main(String[] args) {
        System.out.println("世界你好");
        System.out.println("世界你好");
        System.out.println("世界你好");
        System.out.println("世界你好");
        System.out.println("世界你好");
        //使用for循环代替
        for (int i = 0; i < 5; i++) {
            System.out.println("世界你好");
        }
    }
}
